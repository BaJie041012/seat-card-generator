// ------------------------------------------------------------------------------
// 人员信息数据类
// 功能: 封装单个参会人员的基本信息
// 设计思路: 使用不可变对象（final 字段），确保数据在创建后不会被意外修改
// ------------------------------------------------------------------------------
/// 人员信息数据类
///
/// 类说明:
///   [PersonInfo] 用于封装单个参会人员的完整信息，是系统中最基础的数据单元。
///   从输入的 Excel/CSV 文件中解析出的人员数据会被转换为 PersonInfo 对象，
///   然后在后续的席卡生成流程中使用。
///
/// 属性说明:
///   - [name]: 人员姓名（必填），用于席卡上显示的主要文字
///   - [company]: 所属公司/单位名称（选填），显示在姓名下方或旁边
///   - [position]: 职位/职务（选填），可选的额外信息
///   - [originalLine]: 原始输入行文本（选填），用于调试和溯源
///
/// 使用方式:
///   1. 直接构造: `PersonInfo(name: '张三', company: 'ABC公司')`
///   2. JSON 解析: `PersonInfo.fromJson(jsonMap)` — 从后端 API 返回的 JSON 数据创建
///
/// 设计思路:
///   - 所有字段使用 final 修饰，保证数据不可变性（类似 Python 的 frozen dataclass）
///   - 使用 const 构造函数，支持编译期常量创建，提升性能
///   - 除 name 外其他字段均有默认空字符串值，提高使用的灵活性
class PersonInfo {
  // ==========================================================================
  // 字段定义
  // ==========================================================================

  /// 人员姓名（必填字段）
  /// 这是席卡上显示的核心信息，不能为空
  final String name;

  /// 所属公司/单位名称（选填字段）
  /// 默认为空字符串，当席卡需要显示公司信息时使用
  final String company;

  /// 职位/职务（选填字段）
  /// 默认为空字符串，可选的额外信息，用于更详细的席卡展示
  final String position;

  /// 原始输入行文本（选填字段）
  /// 保留原始数据行的文本内容，主要用于：
  ///   - 调试时追溯数据来源
  ///   - 解析出错时显示原始数据
  ///   - 日志记录
  final String originalLine;

  // ==========================================================================
  // 构造函数
  // ==========================================================================

  /// 命名参数构造函数
  ///
  /// 参数说明:
  ///   [name] - 人员姓名（必填，使用 required 关键字约束）
  ///   [company] - 公司名称（选填，默认为空字符串 ''）
  ///   [position] - 职位（选填，默认为空字符串 ''）
  ///   [originalLine] - 原始输入行（选填，默认为空字符串 ''）
  ///
  /// 设计说明:
  ///   - 使用 const 修饰，支持编译期常量实例化
  ///   - 使用命名参数（named parameters）提高代码可读性
  ///   - 只有 name 是必填参数，其他参数都有合理的默认值
  const PersonInfo({
    required this.name,       // 姓名为必填项，使用 required 关键字强制传入
    this.company = '',        // 公司名称默认空字符串，可选传入
    this.position = '',       // 职位默认空字符串，可选传入
    this.originalLine = '',   // 原始行默认空字符串，可选传入
  });

  // ==========================================================================
  // 工厂构造函数 - JSON 反序列化
  // ==========================================================================

  /// 从 JSON 数据创建 PersonInfo 实例（工厂构造函数）
  ///
  /// 参数说明:
  ///   [json] - 包含人员信息的 JSON 映射（Map），键值对格式如下：
  ///     - 'name': 姓名（字符串）
  ///     - 'company': 公司名称（字符串，可选）
  ///     - 'position': 职位（字符串，可选）
  ///     - 'original_line': 原始输入行（字符串，可选）
  ///
  /// 返回值:
  ///   PersonInfo - 新创建的人员信息实例
  ///
  /// 容错处理:
  ///   - 使用 ?. 空安全操作符防止 null 值导致的异常
  ///   - 使用 ?? 空合并操作符在值为 null 时提供默认空字符串
  ///   - 使用 toString() 确保非字符串类型的值也能正确转换
  ///
  /// 使用场景:
  ///   主要用于从后端 Python API 返回的 JSON 数据中解析人员信息
  factory PersonInfo.fromJson(Map<String, dynamic> json) {
    // 使用工厂构造函数返回新实例
    // 工厂构造函数的优势：可以包含复杂逻辑、返回子类型、支持缓存等
    return PersonInfo(
      // 从 JSON 中提取姓名字段，使用空安全操作符链处理可能的 null 值
      // ?.toString() 先将值转为字符串，?? '' 在整体为 null 时提供默认空字符串
      name: json['name']?.toString() ?? '',
      // 从 JSON 中提取公司名称字段，处理逻辑同上
      company: json['company']?.toString() ?? '',
      // 从 JSON 中提取职位字段，处理逻辑同上
      position: json['position']?.toString() ?? '',
      // 从 JSON 中提取原始行字段，键名为 'original_line'（下划线命名，与 Python 端一致）
      originalLine: json['original_line']?.toString() ?? '',
    );
  }
}

// ------------------------------------------------------------------------------
// 单个席卡生成结果类
// 功能: 封装单个席卡的生成状态和输出信息
// 设计思路: 记录每个席卡的生成是否成功、输出文件路径、以及可能的错误信息
// ------------------------------------------------------------------------------
/// 单个席卡的生成结果
///
/// 类说明:
///   [CardResult] 用于记录单个席卡生成操作的完整结果信息。
///   每次生成一个席卡后，都会创建一个 CardResult 实例来保存该次操作的状态。
///   这些信息会被汇总到 [GenerateResult] 中，用于向用户展示批量生成的结果。
///
/// 属性说明:
///   - [displayName]: 席卡上显示的名称（姓名或公司名），用于 UI 展示
///   - [filePath]: 生成的 PDF 文件在本地文件系统中的绝对路径
///   - [success]: 生成是否成功的布尔标志，true 表示成功
///   - [error]: 生成失败时的错误信息，成功时为 null
///
/// 使用方式:
///   生成席卡后创建实例，检查 success 字段判断是否成功：
///   ```dart
///   if (result.success) {
///     // 打开生成的文件
///   } else {
///     // 显示错误信息
///   }
///   ```
class CardResult {
  // ==========================================================================
  // 字段定义
  // ==========================================================================

  /// 显示的名称（姓名或公司名）
  /// 用于在 UI 界面中展示，让用户知道这个结果对应哪个人
  final String displayName;

  /// 生成的 PDF 文件路径
  /// 存储生成的席卡 PDF 文件在本地文件系统中的绝对路径
  /// 无论生成成功与否都会有路径值（失败时可能指向空文件或不存在）
  final String filePath;

  /// 生成是否成功的标志
  /// true 表示席卡生成成功，false 表示生成过程中出现错误
  /// 默认为 true，只有在发生异常时才设置为 false
  final bool success;

  /// 错误信息（可空字段）
  /// 当 success 为 false 时，此字段包含具体的错误描述信息
  /// 当 success 为 true 时，此字段为 null
  final String? error;

  // ==========================================================================
  // 构造函数
  // ==========================================================================

  /// 命名参数构造函数
  ///
  /// 参数说明:
  ///   [displayName] - 显示名称（必填），用于 UI 展示
  ///   [filePath] - 生成的文件路径（必填），PDF 文件的本地路径
  ///   [success] - 是否成功（选填，默认为 true）
  ///   [error] - 错误信息（选填，默认为 null）
  ///
  /// 设计说明:
  ///   - 使用 const 修饰，支持编译期常量实例化
  ///   - displayName 和 filePath 为必填项，因为每个结果都必须有名称和路径
  ///   - success 默认为 true，因为大多数情况下生成是成功的
  ///   - error 使用可空类型 String?，只在失败时才需要赋值
  const CardResult({
    required this.displayName,  // 显示名称为必填项
    required this.filePath,     // 文件路径为必填项
    this.success = true,        // 默认为成功状态
    this.error,                 // 错误信息默认为 null（无错误）
  });
}

// ------------------------------------------------------------------------------
// 批量生成结果类
// 功能: 汇总所有席卡的生成结果，提供统计信息
// 设计思路: 聚合多个 CardResult，并提供成功/失败计数的便捷访问器
// ------------------------------------------------------------------------------
/// 批量生成结果
///
/// 类说明:
///   [GenerateResult] 用于汇总一次批量生成操作的所有结果。
///   它包含每个席卡的详细结果列表、合并后的 PDF 路径、输出目录信息，
///   以及失败的人员名单。同时提供成功/失败计数的便捷 getter 方法。
///
/// 属性说明:
///   - [cards]: 所有席卡的生成结果列表，每个元素是一个 [CardResult]
///   - [combinedPdfPath]: 所有席卡合并后的 PDF 文件路径（可选）
///   - [outputDir]: 输出文件的存储目录路径
///   - [failedNames]: 生成失败的人员姓名列表
///
/// 计算属性:
///   - [successCount]: 成功生成的席卡数量（通过 getter 动态计算）
///   - [failCount]: 生成失败的席卡数量（通过 getter 动态计算）
///
/// 使用方式:
///   批量生成完成后，通过此对象获取结果摘要：
///   ```dart
///   print('成功: ${result.successCount}, 失败: ${result.failCount}');
///   if (result.combinedPdfPath != null) {
///     // 打开合并后的 PDF
///   }
///   ```
class GenerateResult {
  // ==========================================================================
  // 字段定义
  // ==========================================================================

  /// 所有席卡的生成结果列表
  /// 每个元素对应一个人员的席卡生成结果（成功或失败）
  /// 列表长度等于尝试生成的总人数
  final List<CardResult> cards;

  /// 合并后的 PDF 文件路径（可空字段）
  /// 当所有席卡生成完成后，可以将所有单独的 PDF 合并为一个文件
  /// 此字段存储合并后文件的绝对路径；如果未执行合并操作则为 null
  final String? combinedPdfPath;

  /// 输出文件的存储目录路径
  /// 所有生成的席卡文件（单独 PDF 和合并 PDF）都存放在此目录下
  final String outputDir;

  /// 生成失败的人员姓名列表
  /// 记录所有生成过程中出现错误的人员姓名
  /// 用于在 UI 中向用户展示哪些人的席卡生成失败
  /// 默认为空列表（const []），表示没有失败项
  final List<String> failedNames;

  // ==========================================================================
  // 构造函数
  // ==========================================================================

  /// 命名参数构造函数
  ///
  /// 参数说明:
  ///   [cards] - 席卡结果列表（必填），包含所有生成结果
  ///   [combinedPdfPath] - 合并 PDF 路径（选填，默认为 null）
  ///   [outputDir] - 输出目录路径（必填），生成文件的存放位置
  ///   [failedNames] - 失败名单（选填，默认为空列表）
  ///
  /// 设计说明:
  ///   - 使用 const 修饰，支持编译期常量实例化
  ///   - failedNames 使用 const [] 作为默认值，确保不可变的空列表
  ///   - combinedPdfPath 使用可空类型，因为合并操作是可选的
  const GenerateResult({
    required this.cards,                  // 席卡结果列表为必填项
    this.combinedPdfPath,                 // 合并 PDF 路径可选，默认为 null
    required this.outputDir,              // 输出目录为必填项
    this.failedNames = const [],          // 失败名单默认为空列表
  });

  // ==========================================================================
  // 计算属性（Getter）
  // ==========================================================================

  /// 成功生成的席卡数量（计算属性）
  ///
  /// 返回值:
  ///   int - 成功生成的席卡数量
  ///
  /// 计算逻辑:
  ///   遍历 cards 列表，筛选 success 为 true 的项，返回计数
  ///   使用 Dart 的 where() 高阶函数进行函数式过滤
  int get successCount => cards.where((c) => c.success).length;

  /// 生成失败的席卡数量（计算属性）
  ///
  /// 返回值:
  ///   int - 生成失败的席卡数量
  ///
  /// 计算逻辑:
  ///   遍历 cards 列表，筛选 success 为 false 的项，返回计数
  ///   使用 !c.success 取反来筛选失败项
  int get failCount => cards.where((c) => !c.success).length;
}

// ------------------------------------------------------------------------------
// 显示类型枚举
// 功能: 定义席卡上显示内容的类型选项
// 设计思路: 使用枚举类型限制显示类型的取值范围，避免使用魔法字符串
// ------------------------------------------------------------------------------
/// 显示类型枚举
///
/// 枚举说明:
///   [DisplayType] 用于控制席卡上主要显示的内容类型。
///   用户可以在应用界面中选择席卡显示姓名还是公司名称。
///
/// 枚举值:
///   - [name]: 显示人员姓名（默认选项）
///   - [company]: 显示公司/单位名称
///
/// 使用场景:
///   在席卡生成参数配置中，用户选择显示类型后，
///   生成引擎根据此枚举值决定席卡上的主要文字内容
enum DisplayType {
  /// 显示姓名模式
  /// 席卡上主要显示参会人员的姓名
  name,

  /// 显示公司模式
  /// 席卡上主要显示参会人员所属的公司/单位名称
  company,
}
